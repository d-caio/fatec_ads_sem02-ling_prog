public class Lote1Ex31EstRep {
    static final int INI = 10;
    static final int FIM = 150;
    
    public static void main(String[] args) {
        for (int i = INI; i <= FIM; i++) {
            System.out.println(i + "² = " + (i * i));
            
        }
    }
}
